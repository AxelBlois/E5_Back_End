package com.esiee.tp3.domain;

public abstract class ReferentialEntity extends Entity{
    public String code;
    public String label;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
